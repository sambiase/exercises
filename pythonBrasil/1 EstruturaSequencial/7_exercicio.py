'''
   Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário
'''

area_quadrado = float(input('Medida do lado do quadrado --> '))
total_area_quadrado = area_quadrado.__pow__(2)

print(f'Area do Quadrado: {total_area_quadrado}')
print(f'Dobro da Area do Quadrado: {2*total_area_quadrado}')





